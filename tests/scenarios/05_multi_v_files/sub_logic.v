/* 
 * 【解説: サブモジュール sub_logic】
 * トップモジュールから呼び出され、特定の演算を担当する独立したモジュールです。
 * 複数ファイル構成のビルドと結合を検証するために使用されます。
 */
module sub_logic (
    input wire [31:0] in_a,  // 入力 A
    input wire [31:0] in_b,  // 入力 B
    output wire [31:0] out_y  // 演算結果
);
    /* 
     * 【解説】 
     * 二つの入力に、このファイル固有の定数 0x1234 を加算します。
     * この定数が最終的にソフトウェアから読み出せることで、
     * このファイルが正しくビルド・リンクされたことを証明します。
     */
    assign out_y = in_a + in_b + 32'h1234;
endmodule
